/*
> больше
< меньше
>= больше равно
<= меньше равно
!= не равно
== равно
=== строгое равенство без приведения типа
*/

console.log('elephant' > 'elephant');
/*
Алгоритм сравнения двух строк довольно прост:

1. Сначала сравниваются первые символы строк.
2. Если первый символ первой строки больше (меньше),
чем первый символ второй, то первая строка больше
(меньше) второй. Сравнение завершено.
3. Если первые символы равны, то таким же образом
сравниваются уже вторые символы строк.
4. Сравнение продолжается, пока не закончится
одна из строк.
5. Если обе строки заканчиваются одновременно,
то они равны. Иначе, большей считается более
длинная строка.

*/

console.log('02' == 2);
console.log(undefined === null);
console.log('5' == 5); // true
console.log('5' === 5);  // false, потому что не приводится тип


