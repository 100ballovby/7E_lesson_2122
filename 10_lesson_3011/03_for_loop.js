// счетный цикл for
for (let i = 0; i < 5; i++) { // повторить 5 раз (0, 1, 2, 3, 4)
    console.log(i);  // выводить i
}
/*
i - управляющая переменная цикла. Иногда называется
счетчиком итераций.
Цикл for всегда знает, сколько раз ему нужно повторить действия.
Несмотря на наличие логического оператора, условным этот цикл
не является.
*/

